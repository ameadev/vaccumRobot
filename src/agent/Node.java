package agent;

public class Node {
private int nodeX, nodeY;

	public Node(int x, int y)
	{
		this.nodeX = x;
		this.nodeY = y;
				
	}
	public int getNodeX() {
		return nodeX;
	}
	public void setNodeX(int nodeX) {
		this.nodeX = nodeX;
	}
	public int getNodeY() {
		return nodeY;
	}
	public void setNodeY(int nodeY) {
		this.nodeY = nodeY;
	}
	
}
